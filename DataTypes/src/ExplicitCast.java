
public class ExplicitCast {
	public static void main(String[] args) {
		// Explicit casting
		int i = 100;
		byte b = (byte)i;
		System.out.println(b);
		
		int x = 97;
		char ch = (char)x;
		System.out.println(ch);
		
		// type res = (type) (operation)
		
		int y = 130;
		byte z = (byte)y;
		System.out.println(z);
		
		int m = 612;
		byte n = (byte)m;
		char o = (char)n;
		System.out.println(o);
	}
}
